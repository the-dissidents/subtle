const diacritic: Record<number, number> = {
    0xC1: 0x0300,
    0xC2: 0x0301,
    0xC3: 0x0302,
    0xC4: 0x0303,
    0xC5: 0x0304,
    0xC6: 0x0306,
    0xC7: 0x0307,
    0xC8: 0x0308,
    0xCA: 0x030A,
    0xCB: 0x0327,
    0xCD: 0x030B,
    0xCE: 0x0328,
    0xCF: 0x030C,
};

export class ISO6937Decoder {
    decode(input: Uint8Array): string {
        const output: string[] = [];
        let diacriticState: number | null = null;

        for (let i = 0; i < input.length; i++) {
            const byte = input[i];

            if (diacriticState === null) {
                if (identical.has(byte)) {
                    output.push(String.fromCodePoint(byte));
                } else if (byte in directMapping) {
                    output.push(String.fromCodePoint(directMapping[byte]));
                } else if (byte in diacritic) {
                    diacriticState = diacritic[byte];
                }
            } else {
                const base = String.fromCodePoint(byte);
                const combining = String.fromCodePoint(diacriticState);
                const combined = (base + combining).normalize('NFC');
                if (combined.length === 1) {
                    output.push(combined);
                }
                diacriticState = null;
            }
        }

        return output.join('');
    }
}
